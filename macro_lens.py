@retry(
    wait=wait_exponential(multiplier=1, min=2, max=10),
    stop=stop_after_attempt(3),
    reraise=True,
)
def _invoke_with_retry(graph, state, config):
    return graph.invoke(state, config=config)


def run_backtest(
    start: str = "2010-01-01",
    end: Optional[str] = None,
    progress_callback=None,
    use_cfnai_ma3: bool = True,
    validate_regime: str = "off",
) -> dict:
    """
    Point-in-time backtest: weights[t] applied to returns[t+1], no look-ahead.

    v4 parameters:
        use_cfnai_ma3:   True  → Arm 1b/2/3; False → Arm 1a (v3-compatible)
        validate_regime: "off" → Arm 1 (baseline, fast)
                         "log" → Arm 2 (advisory, populates validator cache)
                         "active" → Arm 3 (active override, fast if Arm 2 ran first)

    Three-arm experiment sequence:
        run_backtest(validate_regime="off",    use_cfnai_ma3=True)  # Arm 1
        run_backtest(validate_regime="log",    use_cfnai_ma3=True)  # Arm 2
        run_backtest(validate_regime="active", use_cfnai_ma3=True)  # Arm 3
    """
    if end is None:
        end = datetime.today().strftime("%Y-%m-%d")

    observation_dates = _monthly_dates(start, end)
    n_dates           = len(observation_dates)

    all_tickers = list(ASSET_PROXIES.values())

    prices          = _fetch_price_history(all_tickers, start, end)
    monthly_returns = prices.pct_change().dropna()

    cov_start   = (datetime.strptime(start, "%Y-%m-%d") - timedelta(days=365 * 5)).strftime("%Y-%m-%d")
    prices_full = _fetch_price_history(all_tickers, cov_start, end)
    returns_df  = prices_full.rename(
        columns={v: k for k, v in ASSET_PROXIES.items()}
    ).pct_change().dropna()

    fred   = Fred(api_key=os.getenv("FRED_API_KEY"))
    engine = RegimeHMMEngine(fred, use_cfnai_ma3=use_cfnai_ma3)
    graph  = build_graph(
        engine, returns_df,
        skip_narrative=True,
        skip_fred=True,
        validate_regime=validate_regime,
    )

    monthly_records = []

    for i, obs_date in enumerate(observation_dates):
        if progress_callback:
            progress_callback(i / n_dates, f"Processing {obs_date}  ({i+1}/{n_dates})")

        config = {"configurable": {"thread_id": str(uuid.uuid4())}}

        initial_state: MacroState = {
            "current_date":               obs_date,
            "observation_date":           obs_date,
            "macro_data":                 None,
            "fetch_attempts":             0,
            "data_requests":              None,
            "growth_direction":           None,
            "inflation_direction":        None,
            "regime":                     None,
            "regime_confidence":          None,
            "regime_rationale":           None,
            "previous_regime":            None,
            "hmm_probabilities":          None,
            "original_hmm_regime":        None,
            "regime_validator_decision":  None,
            "regime_validator_rationale": None,
            "pcepilfe_level":             None,
            "tilts":                      None,
            "weights":                    None,
            "allocation_rationale":       None,
            "report":                     None,
            "messages":                   [],
            "history_dates":              observation_dates[:i + 1],
        }

        try:
            result = _invoke_with_retry(graph, initial_state, config)
        except Exception as e:
            print(f"  WARNING: {obs_date} failed permanently ({e}), using baseline weights")
            result = {
                "weights":                    BASELINE_WEIGHTS.copy(),
                "regime":                     "Unknown",
                "regime_confidence":          0.25,
                "regime_validator_decision":  "SKIPPED",
                "regime_validator_rationale": None,
                "original_hmm_regime":        None,
            }

        monthly_records.append({
            "date":               obs_date,
            "regime":             result.get("regime", "Unknown"),
            "confidence":         result.get("regime_confidence", 0.25),
            "weights":            result.get("weights", BASELINE_WEIGHTS.copy()),
            "validator_decision": result.get("regime_validator_decision", "SKIPPED"),
            "original_regime":    result.get("original_hmm_regime"),
        })

        time.sleep(1)

    if progress_callback:
        progress_callback(0.95, "Computing performance metrics...")

    result_perf = _compute_performance(monthly_records, monthly_returns)

    # Validator decision summary
    if validate_regime != "off":
        decisions = [r["validator_decision"] for r in monthly_records]
        from collections import Counter
        counts = Counter(decisions)
        print(f"\n=== Validator Decision Summary (mode={validate_regime}) ===")
        for decision, count in sorted(counts.items()):
            print(f"  {decision:<10} {count:>4} months")
        overrides = [r for r in monthly_records if r["validator_decision"] == "OVERRIDE"]
        if overrides:
            print(f"\n  OVERRIDE details:")
            vc = _load_validator_cache()
            for r in overrides:
                # Find cache entry by observation_date
                replacement = next(
                    (v.get("replacement_regime") for v in vc.values()
                    if v.get("observation_date") == r["date"]),
                    "unknown"
                )
                print(f"    {r['date']}  {r['original_regime']} → {replacement}")

    # Average weights by regime
    regime_weights = defaultdict(list)
    for record in monthly_records:
        regime_weights[record["regime"]].append(record["weights"])

    print("\n=== Average Weights by Regime ===")
    for regime, weight_list in sorted(regime_weights.items()):
        avg = {k: np.mean([w.get(k, 0.0) for w in weight_list]) for k in BASELINE_WEIGHTS}
        n   = len(weight_list)
        print(f"\n{regime} (n={n} months):")
        for asset, w in avg.items():
            bar = "█" * int(w * 40)
            print(f"  {asset:<22} {w:>6.1%}  {bar}")

    if progress_callback:
        progress_callback(1.0, "Done.")

    return {
        "equity_curve":    result_perf["equity_curve"],
        "benchmark_curve": result_perf["benchmark_curve"],
        "policy_curve":    result_perf["policy_curve"],
        "monthly_records": monthly_records,
        "metrics":         result_perf["metrics"],
    }


if __name__ == "__main__":
    result = run_analysis()
    print(result.get("report", "No report generated"))