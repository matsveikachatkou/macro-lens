"""
macro_lens.py — LangGraph orchestration pipeline for macro-lens v4.

Architecture (v4 hybrid quant/AI):
  data_fetcher          → fetches FRED indicators + VIX for live display
  quant_regime_node     → RegimeHMMEngine classifies growth/inflation regime
  llm_regime_validator  → optional: gates + LLM sanity check on HMM output
  quant_allocation_node → Black-Litterman generates tactical weights
  weight_calculator     → normalises weights to sum to 1.0 (safety net)
  narrative_generator   → LLM writes human-readable rationale (live mode only)
  reporter              → formats the final report string

v4 changes vs v3:
  - RegimeHMMEngine accepts use_cfnai_ma3 flag (passed through run_backtest /
    run_analysis via engine construction)
  - llm_regime_validator node added between quant_regime_node and
    quant_allocation_node; controlled by validate_regime parameter on
    build_graph() and run_backtest()
  - MacroState extended with four validator fields
  - reporter updated to show validator decision when present
  - run_backtest() accepts validate_regime and use_cfnai_ma3 arguments

validate_regime modes:
  "off"    — validator node not added to graph; zero overhead (default in backtest)
  "log"    — validator runs and writes decision to state fields, but does NOT
             modify regime or regime_confidence; weights unchanged vs "off"
  "active" — validator runs; OVERRIDE replaces regime in state; FLAG sets
             regime_confidence to 0.25 (forces BL toward policy weights)

Three-arm experiment:
  Arm 1: run_backtest(validate_regime="off",    use_cfnai_ma3=True)  — structural fixes baseline
  Arm 2: run_backtest(validate_regime="log",    use_cfnai_ma3=True)  — advisory log only
  Arm 3: run_backtest(validate_regime="active", use_cfnai_ma3=True)  — active override

Run Arm 2 before Arm 3 — it populates validator_cache.json so Arm 3 is all cache hits.
"""

