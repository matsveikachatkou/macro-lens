def _load_validator_cache() -> Dict[str, dict]:
    """Load validator decision cache from disk."""
    if not VALIDATOR_CACHE_PATH.exists():
        return {}
    with open(VALIDATOR_CACHE_PATH, "r") as f:
        return json.load(f)


def _save_validator_cache(cache: Dict[str, dict]) -> None:
    """Persist validator decision cache to disk."""
    with open(VALIDATOR_CACHE_PATH, "w") as f:
        json.dump(cache, f, indent=2)


