def build_graph(
    engine: RegimeHMMEngine,
    returns_df: pd.DataFrame,
    skip_narrative: bool = False,
    skip_fred: bool = False,
    validate_regime: str = "off",
):
    """
    Build and compile the LangGraph pipeline.

    v4 parameter:
        validate_regime: "off" | "log" | "active"
            "off"    — validator node not wired in; zero overhead
            "log"    — validator runs, writes to state fields, does NOT
                       modify regime or regime_confidence
            "active" — validator runs; OVERRIDE replaces regime;
                       FLAG sets regime_confidence to 0.25

    Graph topology:
      Backtest (skip_fred=True, skip_narrative=True):
        quant_regime_node → [llm_regime_validator] → quant_allocation_node
        → weight_calculator → reporter

      Live:
        data_fetcher → quant_regime_node → [llm_regime_validator]
        → quant_allocation_node → weight_calculator
        → narrative_generator → reporter
    """
    assert validate_regime in ("off", "log", "active"), (
        f"validate_regime must be 'off', 'log', or 'active', got {validate_regime!r}"
    )

    memory = MemorySaver()

    # Load validator cache once per graph build — shared across all monthly steps
    validator_cache: Dict[str, dict] = (
        _load_validator_cache() if validate_regime != "off" else {}
    )

    # ------------------------------------------------------------------
    def quant_regime_node(state: MacroState) -> dict:
        """Classify the macro regime via RegimeHMMEngine."""
        observation_date = state.get("observation_date") or state["current_date"]

        try:
            result = engine.infer(observation_date)
        except ValueError as e:
            print(f"  HMM insufficient history for {observation_date}: {e}")
            return {
                "regime":               "Low Growth / Low Inflation",
                "regime_confidence":    0.25,
                "growth_direction":     "falling",
                "inflation_direction":  "falling",
                "hmm_probabilities": {
                    "High Growth / High Inflation": 0.25,
                    "High Growth / Low Inflation":  0.25,
                    "Low Growth / High Inflation":  0.25,
                    "Low Growth / Low Inflation":   0.25,
                },
                "pcepilfe_level":              None,
                "regime_validator_decision":   "SKIPPED",
                "regime_validator_rationale":  None,
                "original_hmm_regime":         None,
            }

        if "error" in result:
            print(f"  HMM error for {observation_date}: {result['error']}")
            return {
                "regime":               "Low Growth / Low Inflation",
                "regime_confidence":    0.25,
                "growth_direction":     "falling",
                "inflation_direction":  "falling",
                "hmm_probabilities":    {},
                "pcepilfe_level":              None,
                "regime_validator_decision":   "SKIPPED",
                "regime_validator_rationale":  None,
                "original_hmm_regime":         None,
            }

        dominant    = result["dominant_regime"]
        confidence  = result["confidence"]
        joint_probs = result["joint_probabilities"]
        indicators  = result.get("indicators", {})

        return {
            "regime":               dominant,
            "regime_confidence":    confidence,
            "growth_direction":     "rising" if result["growth_high_prob"] >= 0.5 else "falling",
            "inflation_direction":  "rising" if result["inflation_high_prob"] >= 0.5 else "falling",
            "hmm_probabilities":    joint_probs,
            "previous_regime":      dominant,
            "pcepilfe_level":       indicators.get("pcepilfe_level"),
            # Pre-populate validator fields as SKIPPED — overwritten by
            # llm_regime_validator if the node is wired in
            "regime_validator_decision":  "SKIPPED",
            "regime_validator_rationale": None,
            "original_hmm_regime":        None,
        }

    # ------------------------------------------------------------------
    def llm_regime_validator(state: MacroState) -> dict:
        """
        Optional LangGraph node: gated LLM sanity check on HMM output.

        Gate logic (deterministic, from validator_prompt_FROZEN.py):
          Gate 1 — HMM confidence < 0.65
          Gate 2 — PCE YoY > 2.5% but HMM calls Low Inflation
          Gate 3 — VIX > 35 but HMM calls High Growth

        If no gate fires, returns immediately with decision=SKIPPED.
        If any gate fires, builds blinded input (no calendar date), checks
        validator_cache, and calls gpt-4o-mini on cache miss.

        In "log" mode:  writes decision to state, does not alter regime/confidence.
        In "active" mode: OVERRIDE replaces regime; FLAG sets confidence to 0.25.

        The validator_cache is shared across all monthly steps via closure.
        Cache key = SHA-256 of blinded input dict (not observation date).
        """
        observation_date  = state.get("observation_date") or state["current_date"]
        hmm_regime        = state.get("regime", "Low Growth / Low Inflation")
        hmm_confidence    = state.get("regime_confidence", 0.25)
        hmm_probs         = state.get("hmm_probabilities", {})
        pcepilfe_level    = state.get("pcepilfe_level") or 0.0
        macro_data        = state.get("macro_data") or {}

        # Extract growth/inflation call from dominant regime string
        hmm_inflation_call = (
            "High Inflation" if "High Inflation" in hmm_regime else "Low Inflation"
        )
        hmm_growth_call = (
            "High Growth" if "High Growth" in hmm_regime else "Low Growth"
        )

        # VIX: from macro_data if available (live mode), else NaN (backtest)
        vix_level = 0.0
        if macro_data:
            vix_data = macro_data.get("vix", {})
            if "latest" in vix_data:
                vix_level = float(vix_data["latest"])

        # Gate check — deterministic, no LLM involved
        should_invoke, gates_fired = should_invoke_validator(
            hmm_confidence=hmm_confidence,
            pcepilfe_level=pcepilfe_level,
            hmm_inflation_call=hmm_inflation_call,
            vix_level=vix_level,
            hmm_growth_call=hmm_growth_call,
        )

        if not should_invoke:
            return {
                "regime_validator_decision":  "SKIPPED",
                "regime_validator_rationale": None,
                "original_hmm_regime":        None,
            }

        # Build blinded input — no calendar date passed
        # z-scores come from HMM indicators if available, else NaN
        # In backtest without live FRED fetch, HY spread and yield curve
        # are unavailable; pass 0.0 so gates still work but LLM sees zeros
        hy_spread   = 0.0
        yield_curve = 0.0
        if macro_data:
            hy_data  = macro_data.get("bamlh0a0hym2", {})
            yc_data  = macro_data.get("t10y2y", {})
            if "latest" in hy_data:
                hy_spread   = float(hy_data["latest"])
            if "latest" in yc_data:
                yield_curve = float(yc_data["latest"])

        # Pull z-scores from hmm engine result stored in state
        # These were written by quant_regime_node via the indicators dict
        # We re-read them from the engine cache via state — they're always
        # available because infer() populates indicators regardless of mode
        cfnai_zscore    = 0.0
        unrate_zscore   = 0.0
        pcepilfe_zscore = 0.0
        cpiaucsl_zscore = 0.0
        # Access engine cache directly (engine is closed over in build_graph)
        primary_growth_key = engine.growth_features[0]
        cfnai_zscore    = engine.cache.get((primary_growth_key, observation_date), 0.0) or 0.0
        unrate_zscore   = engine.cache.get(("UNRATE",    observation_date), 0.0) or 0.0
        pcepilfe_zscore = engine.cache.get(("PCEPILFE",  observation_date), 0.0) or 0.0
        cpiaucsl_zscore = engine.cache.get(("CPIAUCSL",  observation_date), 0.0) or 0.0

        blinded = build_blinded_input(
            hmm_dominant_regime    = hmm_regime,
            hmm_confidence         = hmm_confidence,
            hmm_joint_probabilities= hmm_probs,
            growth_high_prob       = hmm_probs.get("High Growth / High Inflation", 0.0)
                                   + hmm_probs.get("High Growth / Low Inflation", 0.0),
            inflation_high_prob    = hmm_probs.get("High Growth / High Inflation", 0.0)
                                   + hmm_probs.get("Low Growth / High Inflation", 0.0),
            pcepilfe_level         = pcepilfe_level,
            pcepilfe_zscore        = pcepilfe_zscore,
            cfnai_zscore           = cfnai_zscore,
            unrate_zscore          = unrate_zscore,
            cpiaucsl_zscore        = cpiaucsl_zscore,
            vix_level              = vix_level,
            hy_spread              = hy_spread,
            yield_curve            = yield_curve,
            gates_fired            = gates_fired,
        )

        cache_key = build_cache_key(blinded)

        # Cache hit — no LLM call needed
        if cache_key in validator_cache:
            decision_dict = validator_cache[cache_key]
            print(f"  Validator cache hit: {cache_key[:8]}… → {decision_dict['decision']}")
        else:
            # Cache miss — call LLM
            print(f"  Validator invoking LLM for {observation_date} "
                  f"(gates: {', '.join(g.split(':')[0] for g in gates_fired)})")
            try:
                llm = ChatOpenAI(model="gpt-4o-mini", temperature=0)
                messages = [
                    SystemMessage(content=SYSTEM_PROMPT),
                    HumanMessage(content=build_user_prompt(blinded)),
                ]
                response      = llm.invoke(messages)
                decision_dict = parse_validator_response(response.content)
            except Exception as e:
                print(f"  Validator LLM call failed ({e}), defaulting to CONFIRM")
                decision_dict = {
                    "decision":               "CONFIRM",
                    "replacement_regime":     None,
                    "rationale":              "",
                    "primary_contradiction":  None,
                    "confidence_in_decision": "LOW",
                    "parse_note":             f"LLM call failed: {e}",
                }

            # Contamination audit — log any markers found
            rationale  = decision_dict.get("rationale", "")
            contam     = audit_rationale_for_contamination(rationale)
            if contam:
                print(f"  WARNING contamination markers in rationale: {contam}")
            decision_dict["contamination_markers"] = contam
            decision_dict["observation_date"]      = observation_date
            decision_dict["prompt_version"]        = PROMPT_VERSION
            decision_dict["gates_fired"]           = gates_fired
            decision_dict["blinded_input_summary"] = {
                "pce_level":      blinded["pcepilfe_yoy_pct"],
                "vix":            blinded["vix"],
                "hmm_confidence": blinded["hmm_confidence"],
            }

            validator_cache[cache_key] = decision_dict
            _save_validator_cache(validator_cache)
            time.sleep(0.5)

        decision           = decision_dict["decision"]
        replacement_regime = decision_dict.get("replacement_regime")
        rationale          = decision_dict.get("rationale", "")

        # Build return dict — always write audit fields
        out: dict = {
            "original_hmm_regime":        hmm_regime,
            "regime_validator_decision":  decision,
            "regime_validator_rationale": rationale,
        }

        # In "active" mode: apply decision to regime/confidence
        if validate_regime == "active":
            if decision == "OVERRIDE" and replacement_regime:
                print(f"  Validator OVERRIDE: {hmm_regime} → {replacement_regime}")
                out["regime"] = replacement_regime
                # Recalculate growth/inflation direction from replacement
                out["inflation_direction"] = (
                    "rising" if "High Inflation" in replacement_regime else "falling"
                )
                out["growth_direction"] = (
                    "rising" if "High Growth" in replacement_regime else "falling"
                )
            elif decision == "FLAG":
                print(f"  Validator FLAG: confidence capped at 0.25 for {observation_date}")
                out["regime_confidence"] = 0.25

        return out

    # ------------------------------------------------------------------
    def quant_allocation_node(state: MacroState) -> dict:
        """Generate tactical weights via Black-Litterman."""
        observation_date = state.get("observation_date") or state["current_date"]
        regime           = state.get("regime", "Low Growth / Low Inflation")
        confidence       = state.get("regime_confidence", 0.25)

        obs_dt            = pd.Timestamp(observation_date)
        available_returns = returns_df[returns_df.index <= obs_dt].tail(COV_WINDOW_MONTHS)

        if len(available_returns) < 12:
            print(f"  BL insufficient return history for {observation_date}, using baseline")
            return {"weights": BASELINE_WEIGHTS.copy()}

        try:
            weights = generate_target_weights(available_returns, regime, confidence)
        except Exception as e:
            print(f"  BL failed for {observation_date}: {e}, using baseline")
            weights = BASELINE_WEIGHTS.copy()

        return {"weights": weights}

    # ------------------------------------------------------------------
    def narrative_generator(state: MacroState) -> dict:
        """LLM analyst node — explains the HMM regime call and BL weights."""
        llm = ChatOpenAI(model="gpt-4o-mini", temperature=0)

        regime             = state.get("regime", "Unknown")
        confidence         = state.get("regime_confidence", 0.0)
        weights            = state.get("weights", {})
        hmm_probs          = state.get("hmm_probabilities", {})
        macro_data         = state.get("macro_data", {})
        obs_date           = state.get("current_date", "")
        original_regime    = state.get("original_hmm_regime")
        validator_decision = state.get("regime_validator_decision", "SKIPPED")

        indicator_text = ""
        for key, val in macro_data.items():
            if isinstance(val, dict) and "error" not in val:
                indicator_text += (
                    f"\n  {key.upper()}: latest={val.get('latest')}, "
                    f"change={val.get('change')}, as_of={val.get('date')}"
                )

        weights_text = ", ".join(
            f"{asset.replace('_', ' ').title()} {w:.1%}"
            for asset, w in weights.items()
        )
        probs_text = ", ".join(f"{r}: {p:.1%}" for r, p in hmm_probs.items())

        # Build regime context — distinguish HMM call from validator override
        if (validator_decision == "OVERRIDE"
                and original_regime
                and original_regime != regime):
            regime_context = (
                f"The HMM originally classified the regime as "
                f"{original_regime} with {confidence:.1%} confidence. "
                f"The LLM validator overrode this classification to {regime} "
                f"because PCE YoY was above the Fed's 2% target while the HMM "
                f"called Low Inflation — a known structural limitation of the "
                f"rolling z-score window. The final regime used for allocation is {regime}."
            )
            task1_instruction = (
                f"Explain why the HMM originally called {original_regime} "
                f"(cite the specific indicators), then explain why the validator "
                f"correctly overrode this to {regime} (cite the PCE level vs Fed target). "
                f"Be specific about the z-score anchoring issue."
            )
        elif validator_decision == "FLAG":
            regime_context = (
                f"The HMM classified the regime as {regime} with {confidence:.1%} "
                f"confidence, but the validator flagged low conviction. "
                f"Policy weights were applied as a result."
            )
            task1_instruction = (
                f"Explain which indicators caused the HMM to call {regime} "
                f"and why the validator flagged low conviction."
            )
        else:
            regime_context = (
                f"The HMM classified the regime as {regime} "
                f"with {confidence:.1%} confidence."
            )
            task1_instruction = (
                f"Explain which specific macro indicators caused the HMM to "
                f"classify {regime} with {confidence:.1%} confidence."
            )

        system_message = """You are a quantitative macro analyst writing a concise executive
    summary for a portfolio manager.

    Your role is strictly to EXPLAIN the output of a statistical model pipeline —
    you are NOT making allocation decisions. The Hidden Markov Model (HMM) has
    already classified the regime, and the Black-Litterman optimizer has already
    calculated the weights. Your job is to explain WHY, using the macro indicators.

    Write in clear, institutional language. Be specific about which indicators
    drove the regime call. Do not suggest alternative allocations."""

        user_message = f"""Date: {obs_date}

    REGIME CLASSIFICATION:
    {regime_context}
    Joint probabilities: {probs_text}

    BLACK-LITTERMAN WEIGHTS:
    {weights_text}

    MACRO INDICATORS:
    {indicator_text}

    Task 1 — Regime Rationale (2-3 sentences):
    {task1_instruction}

    Task 2 — Allocation Rationale (2-3 sentences):
    Explain why the Black-Litterman model tilted toward the above weights
    given this regime, referencing the Bridgewater framework."""

        messages = [
            SystemMessage(content=system_message),
            HumanMessage(content=user_message),
        ]

        response  = llm.invoke(messages)
        full_text = response.content

        if "Task 2" in full_text or "Allocation Rationale" in full_text:
            parts = full_text.split("\n\n")
            mid   = len(parts) // 2
            regime_rationale     = "\n\n".join(parts[:mid]).strip()
            allocation_rationale = "\n\n".join(parts[mid:]).strip()
        else:
            regime_rationale     = full_text
            allocation_rationale = ""

        return {
            "regime_rationale":     regime_rationale,
            "allocation_rationale": allocation_rationale,
            "messages":             messages,
        }

    # ------------------------------------------------------------------
    # Wire the graph
    # ------------------------------------------------------------------
    graph_builder = StateGraph(MacroState)

    graph_builder.add_node("quant_regime_node",     quant_regime_node)
    graph_builder.add_node("quant_allocation_node", quant_allocation_node)
    graph_builder.add_node("weight_calculator",     weight_calculator)
    graph_builder.add_node("reporter",              reporter)

    if not skip_fred:
        graph_builder.add_node("data_fetcher", data_fetcher)
    if not skip_narrative:
        graph_builder.add_node("narrative_generator", narrative_generator)
    if validate_regime != "off":
        graph_builder.add_node("llm_regime_validator", llm_regime_validator)

    # Entry point
    if skip_fred:
        graph_builder.add_edge(START, "quant_regime_node")
    else:
        graph_builder.add_edge(START,          "data_fetcher")
        graph_builder.add_edge("data_fetcher", "quant_regime_node")

    # regime → [validator] → allocation
    if validate_regime != "off":
        graph_builder.add_edge("quant_regime_node",     "llm_regime_validator")
        graph_builder.add_edge("llm_regime_validator",  "quant_allocation_node")
    else:
        graph_builder.add_edge("quant_regime_node", "quant_allocation_node")

    graph_builder.add_edge("quant_allocation_node", "weight_calculator")

    if skip_narrative:
        graph_builder.add_edge("weight_calculator", "reporter")
    else:
        graph_builder.add_edge("weight_calculator",   "narrative_generator")
        graph_builder.add_edge("narrative_generator", "reporter")

    graph_builder.add_edge("reporter", END)

    return graph_builder.compile(checkpointer=memory)


