def run_analysis(
    use_cfnai_ma3: bool = True,
    validate_regime: str = "log",
):
    """
    Live mode: fresh RegimeHMMEngine + fresh ETF returns per call.

    v4 defaults: use_cfnai_ma3=True, validate_regime="log" (advisory only).
    Pass validate_regime="active" to enable autonomous override.
    """
    fred   = Fred(api_key=os.getenv("FRED_API_KEY"))
    engine = RegimeHMMEngine(fred, use_cfnai_ma3=use_cfnai_ma3)

    tickers = list(ASSET_PROXIES.values())
    prices  = _fetch_price_history(
        tickers,
        start=(datetime.now() - timedelta(days=365 * 6)).strftime("%Y-%m-%d"),
        end=datetime.now().strftime("%Y-%m-%d"),
    )
    returns_df = prices.rename(
        columns={v: k for k, v in ASSET_PROXIES.items()}
    ).pct_change().dropna()

    graph  = build_graph(engine, returns_df, validate_regime=validate_regime)
    config = {"configurable": {"thread_id": str(uuid.uuid4())}}

    obs_date = datetime.now().strftime("%Y-%m-%d")
    initial_state: MacroState = {
        "current_date":               obs_date,
        "observation_date":           None,
        "macro_data":                 None,
        "fetch_attempts":             0,
        "data_requests":              None,
        "growth_direction":           None,
        "inflation_direction":        None,
        "regime":                     None,
        "regime_confidence":          None,
        "regime_rationale":           None,
        "previous_regime":            None,
        "hmm_probabilities":          None,
        "original_hmm_regime":        None,
        "regime_validator_decision":  None,
        "regime_validator_rationale": None,
        "pcepilfe_level":             None,
        "tilts":                      None,
        "weights":                    None,
        "allocation_rationale":       None,
        "report":                     None,
        "messages":                   [],
        "history_dates":              None,
    }

    return graph.invoke(initial_state, config=config)


